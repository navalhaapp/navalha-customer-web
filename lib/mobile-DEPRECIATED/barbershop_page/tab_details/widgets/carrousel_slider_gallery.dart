import 'package:carousel_slider/carousel_slider.dart';
import 'package:flutter/material.dart';
import 'package:navalha/core/assets.dart';
import 'package:navalha/core/images_s3.dart';
import 'package:navalha/shared/model/barber_shop_model.dart';
import 'package:navalha/shared/utils.dart';

class CarrouselSliderGallery extends StatefulWidget {
  const CarrouselSliderGallery({super.key, required this.galleyImages});

  final List<GalleryImages>? galleyImages;

  @override
  CarrouselSliderGalleryState createState() => CarrouselSliderGalleryState();
}

class CarrouselSliderGalleryState extends State<CarrouselSliderGallery> {
  @override
  Widget build(BuildContext context) {
    Size size = MediaQuery.of(context).size;
    return !isNullOrEmpty(widget.galleyImages)
        ? Column(
            children: [
              SizedBox(
                height: size.height * 0.25,
                width: size.width,
                child: CarouselSlider.builder(
                  options: CarouselOptions(
                    aspectRatio: 14 / 10,
                    viewportFraction: 0.85,
                    initialPage: 0,
                    enableInfiniteScroll: false,
                    reverse: false,
                    autoPlay: false,
                    autoPlayInterval: const Duration(seconds: 3),
                    autoPlayAnimationDuration:
                        const Duration(milliseconds: 800),
                    autoPlayCurve: Curves.fastOutSlowIn,
                    enlargeCenterPage: true,
                    onPageChanged: (index, reason) {},
                    scrollDirection: Axis.horizontal,
                  ),
                  itemCount: widget.galleyImages!.length,
                  itemBuilder:
                      (BuildContext context, int index, int realIndex) {
                    return Card(
                      elevation: 0,
                      shape: RoundedRectangleBorder(
                        borderRadius: BorderRadius.circular(10),
                      ),
                      child: ClipRRect(
                        borderRadius: BorderRadius.circular(10),
                        child: FadeInImage.assetNetwork(
                          width: size.width,
                          placeholder: imgLoading3,
                          image:
                              '$baseUrlS3bucketGallery${widget.galleyImages![index].barbershopImage!}',
                          fit: BoxFit.cover,
                          fadeInDuration: const Duration(milliseconds: 500),
                          fadeInCurve: Curves.easeIn,
                        ),
                      ),
                    );
                  },
                ),
              ),
              const SizedBox(height: 10)
            ],
          )
        : const SizedBox();
  }
}

